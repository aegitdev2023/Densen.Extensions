using System;

namespace AME.Util
{
    public static class ColsName
    {

        //C# Excel数字转字母列标
        public static string 数字转字母列标(int index)
        {
            var dividend = index;
            var columnName = string.Empty;

            while (dividend > 0)
            {
                var modulo = (dividend - 1) % 26;
                columnName = Convert.ToChar(65 + modulo) + columnName;
                dividend = (dividend - modulo) / 26;
            }

            return columnName;
        }
    }
}
